let lang = localStorage.getItem('kc_lang') || 'en';

function applyLang(l) {
  lang = l;
  localStorage.setItem('kc_lang', l);

  document.querySelectorAll('[data-t]').forEach(el => {
    const key = el.getAttribute('data-t');
    if (translations[l][key] !== undefined) {
      el.textContent = translations[l][key];
    }
  });

  document.getElementById('langEN')?.classList.toggle('active', l === 'en');
  document.getElementById('langMR')?.classList.toggle('active', l === 'mr');
  document.documentElement.lang = l === 'mr' ? 'mr' : 'en';
}

function setLanguage(l) { applyLang(l); }

document.addEventListener('DOMContentLoaded', () => applyLang(lang));